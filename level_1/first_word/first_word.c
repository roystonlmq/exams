#include <unistd.h>

int	main(int argc, char **argv)
{
	if (argc == 2)
	{
		char	*str;
		int		i;

		i = 0;
		str = argv[1];
		while (str[i] == ' ' || str[i] == '\t')
			i++;
		while ((str[i] != ' ' && str[i] != '\t'  && str[i]))
		{
			write(1, &str[i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
}
